public class DataMonth {
    String itemName;
    boolean isExpenseMonth;
    int quantity;
    int sumOfOne;

    public DataMonth(String itemName, boolean isExpenseMonth, int quantity, int sumOfOne) {
        this.itemName = itemName;
        this.isExpenseMonth = isExpenseMonth;
        this.quantity = quantity;
        this.sumOfOne = sumOfOne;
    }
}
